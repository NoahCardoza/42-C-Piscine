void print_bits(unsigned char c)
{
	int i;
	char o;

	i = 128;
	while (i)
	{
		o = ('0' + !!(c & i));
		write(1, &o, 1); 
		i >>= 1;
	}
}

int main(void)
{
	print_bits(50);
}